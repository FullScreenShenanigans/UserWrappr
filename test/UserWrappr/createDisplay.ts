import { expect } from "chai";
import * as sinon from "sinon";

import { IMenuSchema } from "../../src/Menus/MenuSchemas";
import { OptionType } from "../../src/Menus/Options/OptionSchemas";
import { formatHtml } from "../html";
import { it } from "../main";
import { stubUserWrappr } from "./stubs";

it("adds contents to the container", async () => {
    // Arrange
    const { contents, container, userWrapper } = stubUserWrappr();

    // Act
    await userWrapper.createDisplay(container);

    // Assert
    expect(contents.parentElement).to.be.equal(container);
});

it("mirrors the html generated by the fake menus", async () => {
    // Arrange
    const { contents, classNames, container, userWrapper } = stubUserWrappr();

    // Act
    await userWrapper.createDisplay(container);

    // Assert
    expect(container.innerHTML).to.be.equal(formatHtml(`
        <div class="${classNames.outerArea}">
            <div class="${classNames.innerArea}">
            </div>
        </div>
        ${contents.outerHTML}
    `));
});

it("adds menu titles to the container when given menus", async () => {
    // Arrange
    const menus: IMenuSchema[] = [
        {
            options: [],
            title: "abc"
        },
        {
            options: [],
            title: "def"
        }
    ];
    const { contents, classNames, container, userWrapper } = stubUserWrappr({ menus });

    // Act
    await userWrapper.createDisplay(container);

    // Assert
    expect(container.innerHTML).to.be.equal(formatHtml(`
        <div class="${classNames.outerArea}">
            <div class="${classNames.innerArea}">
                <div class="${classNames.menu} ${classNames.menu}-Closed">
                    <div class="${classNames.options}"></div>
                    <div class="${classNames.menuTitle}">
                        ${menus[0].title}
                    </div>
                </div>
                <div class="${classNames.menu} ${classNames.menu}-Closed">
                    <div class="${classNames.options}"></div>
                    <div class="${classNames.menuTitle}">
                        ${menus[1].title}
                    </div>
                </div>
            </div>
        </div>
        ${contents.outerHTML}
    `));
});

it("adds an option view when a menu schema contains an option", async () => {
    // Arrange
    const menus: IMenuSchema[] = [
        {
            options: [
                {
                    action: sinon.spy(),
                    title: "action",
                    type: OptionType.Action
                }
            ],
            title: "abc"
        }
    ];
    const { contents, classNames, container, userWrapper } = stubUserWrappr({ menus });

    // Act
    await userWrapper.createDisplay(container);

    // Assert
    expect(container.innerHTML).to.be.equal(formatHtml(`
        <div class="${classNames.outerArea}">
            <div class="${classNames.innerArea}">
                <div class="${classNames.menu} ${classNames.menu}-Closed">
                    <div class="${classNames.options}">
                        <button name="action">
                            ${menus[0].options[0].title}
                        </button>
                    </div>
                    <div class="${classNames.menuTitle}">
                        ${menus[0].title}
                    </div>
                </div>
            </div>
        </div>
        ${contents.outerHTML}
    `));
});
